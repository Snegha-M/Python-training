'''
    Sequence data types:
        Sequence Types - String,lists,tuples and range objects
'''

#String
a="Hello!"
print(a)

#list - Lists are used to store multiple items in a single variable(changable)
a=[1,2,3]
print(a)

#tuple - Tuples are used to store multiple items in a single variable(unchangeable
a=(1,2,3)
print(a)

#range -  returns a sequence of numbers
a=range(6)
print(a)