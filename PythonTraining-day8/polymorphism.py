'''
    Polymorphism:
        The word polymorphism means having many forms.
        In programming, polymorphism means the same function name (but different signatures)
        being used for different types.
'''
# Polymorphism in addition operator
#  + operator is used extensively in Python programs. But, it does not have a single usage.
num1 = 1
num2 = 2
print(num1+num2)

# + operator is used to perform concatenation.
str1 = "Hi"
str2 = "everyone"
print(str1+" "+str2)

'''
     we can see that a single operator + has been used to carry out different operations for distinct data types. 
     This is one of the most simple occurrences of polymorphism in Python.
'''

