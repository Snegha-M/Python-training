'''
    Numeric data type:
        numeric types - integers,floating point numbers and complex numbers.
'''

#integer - is a whole number, positive or negative, without decimals, of unlimited length.
a=1
print(a)

#float - is a number, positive or negative, containing one or more decimals.
a=1.1
print(a)

#complex - Complex numbers are written with a "j" as the imaginary par
a=1j
print(a)

#bool - booleans represent one of two values: True or False
x=True
if x:
    print("x value is true")